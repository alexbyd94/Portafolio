
    function mouseEnter() {
        document.getElementById("a").style.textShadow  = "0 0 30px #fff";
        document.getElementById("link").style.textShadow  = "0 0 30px #fff";
}

    function mouseLeave() {
    document.getElementById("a").style.textShadow  = "none";
    document.getElementById("link").style.textShadow = "none";
}
